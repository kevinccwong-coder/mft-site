「贏在起跑線」不僅傷害孩子，也系統性地瓦解家庭：

**手足之間的「缺陷／羞恥」競賽**：當父母的認可有且只有一個標準（成就），兄弟姐妹不再是同盟，而是資源的競爭者。成績較差的孩子終生背負「我不如他／她」的羞恥（缺陷／羞恥基模）；成績好的孩子則活在「下一次可能就輸了」的恐懼中（被遺棄基模）。

